(*......................................................................
  Environments and values 
 *)

module type ENV = sig
    (* the type of environments *)
    type env
    (* the type of values stored in environments *)
    type value =
      | Val of expr
      | Closure of (expr * env)
   
    (* empty () -- Returns an empty environment *)
    val empty : unit -> env

    (* close expr env -- Returns a closure for `expr` and its `env` *)
    val close : expr -> env -> value

    (* lookup env varid -- Returns the value in the `env` for the
       `varid`, raising an `Eval_error` if not found *)
    val lookup : env -> varid -> value

    (* extend env varid loc -- Returns a new environment just like
       `env` except that it maps the variable `varid` to the `value`
       stored at `loc`. This allows later changing the value, an
       ability used in the evaluation of `letrec`. To make good on
       this, extending an environment needs to preserve the previous
       bindings in a physical, not just structural, way. *)
    val extend : env -> varid -> value ref -> env

    (* env_to_string env -- Returns a printable string representation
       of environment `env` *)
    val env_to_string : env -> string
                                 
    (* value_to_string ?printenvp value -- Returns a printable string
       representation of a value; the optional flag `printenvp`
       (default: `true`) determines whether to include the environment
       in the string representation when called on a closure *)
    val value_to_string : ?printenvp:bool -> value -> string
  end

module Env : ENV =
  struct
    type env = (varid * value ref) list
     and value =
       | Val of expr
       | Closure of (expr * env)

    let empty () : env = []

    let close (exp : expr) (env : env) : value =
      Closure (exp, env)

    let lookup (env : env) (varname : varid) : value =
      match List.find_opt (fun (var, _val_ref) -> var = varname) env with
      | Some (_var, val_ref) -> !val_ref
      | None -> raise (EvalError "lookup: varid not found")

    let extend (env : env) (varname : varid) (loc : value ref) : env =
      (varname, loc) :: List.filter (fun (var, _val_ref) -> var <> varname) env

    let rec value_to_string ?(printenvp : bool = true) (v : value) : string =
      match v with
      | Val exp -> exp_to_concrete_string exp
      | Closure (exp, env) ->
        let exp_str = exp_to_concrete_string exp in
        if printenvp then "[" ^ env_to_string env ^ " âŠ¢ " ^ exp_str ^ "]"
        else exp_str
    and env_to_string (env : env) : string =
      let count = ref (List.length env) in
      let str_contents =
        List.fold_left
          (fun acc (var, val_ref) ->
             let substr = acc ^ var ^  " -> " ^ value_to_string !val_ref in
             if !count <= 1 then substr
             else (count := !count - 1; substr ^ "; "))
          "" env
      in "{" ^ str_contents ^ "}"

